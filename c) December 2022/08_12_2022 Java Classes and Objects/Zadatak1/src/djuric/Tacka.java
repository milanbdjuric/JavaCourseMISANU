package djuric;

public class Tacka {
	
	private double x, y;
	
	Tacka(double xx, double yy) {
		x = xx;
		y = yy;
	}
	
	public double getX() { // uzimlje, dofata vrednost za koordinatu x
		return x;
	}
	
	public double getY() {
		return y;
	}
	
	public String ispisi() {
		return "(" + x + ", " + y + ")";
	
	
	
	
	
	
	}
	

}
