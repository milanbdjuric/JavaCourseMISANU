import java.io.*;
import java.text.DecimalFormat;

public class Zadatak2 {

	public static void main(String[] args) throws Exception {
		
		BufferedReader input = new BufferedReader(new InputStreamReader(System.in));
		DecimalFormat deci = new DecimalFormat("#.##");
		
		double x, y, z, f;
		
		System.out.println("Unesite vrednost promenljive x: ");
		x = Double.parseDouble(input.readLine());
		System.out.println("Unesite vrednost promenljive y: ");
		y = Double.parseDouble(input.readLine());
		System.out.println("Unesite vrednost promenljive z: ");
		z = Double.parseDouble(input.readLine());
		
		f = ((x - 2*y)*(x + z)) / (2*x + y);
		
		System.out.println("f = " + deci.format(f));
		
		

	}

}
